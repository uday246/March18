package Mar20th;

public class Staff {
String name;
double salary;
public Staff(String aName, double aSalary) {
	super();
	name = aName;
	salary = aSalary;
}
public String getName() {
	return name;
}
public void setName(String aName) {
	name = aName;
}
public double getSalary() {
	return salary;
}
public void setSalary(double aSalary) {
	salary = aSalary;
}

}
